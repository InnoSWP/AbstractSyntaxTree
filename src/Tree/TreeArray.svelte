<script lang="ts">
    import TreeObject from "./TreeObject.svelte";
    import { slide } from 'svelte/transition';
    import { highlight, deselectAll } from '../MarkerPlugin.svelte'

    export let expanded = true
    export let key = "", value = []
    $: if(key == 'range') expanded = false;

    function calculateRange(): {from: number, to:number} {
        let from, to
        for(let el of value) {
            if(typeof el != 'object') {
                continue;
            }

            if(!from) {
                from = el.range[0]
            }
            if(!to) {
                to = el.range[0]
            }
            from = Math.min(from, el.range[0])
            to = Math.max(to, el.range[1])
        }

        return {from:from, to:to}
    }

    function toggle() {
        expanded = !expanded
    }

    function isPrimitive(val) {
        if(val == null) {
            return true;
        }

        if(typeof val == "object" || Array.isArray(val)) {
            return false;
        }
        
        return true;
    }

    function isObject(val) {
        return typeof val == "object";
    }
</script>

<li class="entry togglable {expanded ? "open" : ""}">
    <span class="key" on:click={toggle} on:mouseenter={() => highlight(calculateRange())} on:mouseleave={()=>deselectAll()}>
        <span class="name nb">
            {key}
        </span>
        <span class="p">{': '}</span>
    </span>
    <span class="prefix p">{" ["}</span>
    {#if expanded}
        <ul class="value-body" transition:slide|local>
            {#each value as e}
                {#if isPrimitive(e)}
                    <li>
                        <span class="entry">
                            <span class="value">
                                <span class="s">{e}</span>
                            </span>
                        </span>
                    </li>
                {:else}
                    <TreeObject obj={e} />
                {/if}
            {/each}
        </ul>
    {/if}
    <div class="suffix p">{"]"}</div>
</li>